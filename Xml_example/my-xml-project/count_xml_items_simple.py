"""15. Подсчёт элементов в XML
Задание: Подсчитайте количество элементов <item> в XML-документе."""
# Импортируем библиотеку для работы с XML
import xml.etree.ElementTree as ET

# Имя нашего XML-файла (замените на имя вашего файла)
xml_file = "test.xml"

# Читаем XML-файл
tree = ET.parse(xml_file)

# Получаем корневой элемент
root = tree.getroot()

# Находим все элементы <item> на любом уровне вложенности
items = root.findall(".//item")

# Выводим количество найденных элементов
print(f"Количество элементов <item>: {len(items)}")
