"""15. Запись JSON в файл
Задание: Создайте JSON-объект и сохраните его в файл data.json."""
import json
# 1. Создаем словарь с данными (как список покупок)
my_data = {
    "покупки": [
        {"продукт": "молоко", "количество": 2, "единица": "литр"},
        {"продукт": "яблоки", "количество": 5, "единица": "штуки"},
        {"продукт": "хлеб", "количество": 3, "единица": "буханка"}
    ],
    "магазин": "Супермаркет",
    "дата": "2024-02-25"
}
# 2. Указываем имя файла, в который сохраняем
file_name = "data.json"

# 3. Открываем файл для записи
with open(file_name, 'w', encoding='utf-8') as json_file:
    # 4. Превращаем словарь в JSON и записываем в файл
    json.dump(my_data, json_file, ensure_ascii=False, indent=4)

# 5. Говорим, что все сделали
print(f"JSON данные записаны в файл {file_name}")

