"""17. Обработка ошибки в JSON
Задание: Обработайте ошибку при разборе некорректного JSON.
Некорректный JSON:
{
  "name": "Иван",
  "age": 25,
}
"""
import json

# 1. Некорректный JSON в виде строки
incorrect_json_string = """
{
  "name": "Иван",
  "age": 25,
}
"""  # Ошибка: лишняя запятая после 25

# 2. Попробуем разобрать JSON и обработать ошибку
try:
    # Пытаемся преобразовать JSON-строку в словарь
    data = json.loads(incorrect_json_string)
    # Если JSON правильный, выведем результат
    print("JSON успешно разобран:")
    print(data)
except json.JSONDecodeError:
    # Если JSON неправильный, сработает этот блок кода
    print("Ошибка: Некорректный JSON")



