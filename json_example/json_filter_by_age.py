"""11. Фильтрация массива JSON
Задание: В массиве сотрудников найдите всех, чей возраст старше 30 лет."""
import json

# 1. Наш массив JSON в виде строки
json_string = '''
[
  {
    "имя": "Иван",
    "возраст": 35,
    "должность": "Разработчик"
  },
  {
    "имя": "Мария",
    "возраст": 28,
    "должность": "Дизайнер"
  },
  {
    "имя": "Петр",
    "возраст": 42,
    "должность": "Менеджер"
  }
]
'''

# 2. Преобразуем JSON в Python-объект
data = json.loads(json_string)

# 3. Проверяем и выводим сотрудников
print("Сотрудники старше 30 лет:")
if data[0]["возраст"] > 30:
  print(data[0])
if data[1]["возраст"] > 30:
  print(data[1])
if data[2]["возраст"] > 30:
  print(data[2])