"""18. Преобразование JSON в таблицу
Задание: Преобразуйте JSON-массив в таблицу (например, в HTML-таблицу или таблицу данных в Python)."""
import json

# 1. Наш JSON-массив (пример)
json_data = """
[
    {"имя": "Иван", "возраст": 30},
    {"имя": "Мария", "возраст": 25}
]
"""

# 2. Преобразуем JSON в Python-объект (список словарей)
data = json.loads(json_data)

# 3. HTML-таблица (собираем вручную)
html_table = "<table><tr><th>Имя</th><th>Возраст</th></tr>" # Заголовки
html_table += f"<tr><td>{data[0]['имя']}</td><td>{data[0]['возраст']}</td></tr>" # Первая строка
html_table += f"<tr><td>{data[1]['имя']}</td><td>{data[1]['возраст']}</td></tr>" # Вторая строка
html_table += "</table>"

# 4. Выводим готовую таблицу
print(html_table)